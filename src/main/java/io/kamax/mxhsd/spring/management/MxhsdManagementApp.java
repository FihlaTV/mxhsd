package io.kamax.mxhsd.spring.management;

import io.kamax.mxhsd.spring.management.config.ManagementConnectorConfig;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.context.embedded.EmbeddedServletContainerFactory;
import org.springframework.boot.context.embedded.tomcat.TomcatConnectorCustomizer;
import org.springframework.boot.context.embedded.tomcat.TomcatEmbeddedServletContainerFactory;
import org.springframework.context.annotation.Bean;

@SpringBootApplication
public class MxhsdManagementApp {

    private ManagementConnectorConfig cfg;

    @Autowired
    public MxhsdManagementApp(ManagementConnectorConfig cfg) {
        this.cfg = cfg;
    }

    @Bean
    public EmbeddedServletContainerFactory servletContainer() {
        TomcatEmbeddedServletContainerFactory tomcat = new TomcatEmbeddedServletContainerFactory();

        tomcat.addConnectorCustomizers((TomcatConnectorCustomizer) connector -> {
            connector.setScheme("http");
            connector.setPort(cfg.getPort());
        });

        return tomcat;
    }

}
